package dsaWithJava.functions.Strings;

public class PrettyPrinting {
    public static void main(String[] args) {
        float z = 34.23492f;
        System.out.printf("Formatted number is %.3f", z);
        System.out.println();
        System.out.printf("Pie : %.4f", Math.PI);
        System.out.println();
        System.out.printf("Hello my name is %s, and i am %s", "Taher", "Awesome!!!!!!!!");
    }
}
